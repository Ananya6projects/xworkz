class Atom {
    public static void defineStructure() {
        System.out.println("Defining the structure of an atom");
    }
    public static void identifyProtons() {
        System.out.println("Identifying the number of protons in the atom");
    }
    public static void identifyNeutrons() {
        System.out.println("Identifying the number of neutrons in the atom");
    }
    public static void identifyElectrons() {
        System.out.println("Identifying the number of electrons in the atom");
    }
    public static void determineAtomicMass() {
        System.out.println("Determining the atomic mass of the atom");
    }
    public static void checkStability() {
        System.out.println("Checking whether the atom is stable or radioactive");
    }
    public static void formMolecule() {
        System.out.println("Forming a molecule by bonding atoms together");
    }
    public static void splitNucleus() {
        System.out.println("Splitting the atomic nucleus in nuclear fission");
    }

    public static void main(String[] args) {
        defineStructure();
        identifyProtons();
        identifyNeutrons();
        identifyElectrons();
        determineAtomicMass();
        checkStability();
        formMolecule();
        splitNucleus();
    }
}
