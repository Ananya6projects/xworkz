class Supra {
    public static void startEngine() {
        System.out.println("Starting the Supra engine");
    }
    public static void accelerate() {
        System.out.println("Accelerating the Supra");
    }
    public static void applyBrakes() {
        System.out.println("Applying brakes to the Supra");
    }
    public static void drift() {
        System.out.println("Drifting the Supra on the track");
    }
    public static void boostTurbo() {
        System.out.println("Activating turbo boost");
    }
    public static void checkFuelLevel() {
        System.out.println("Checking fuel level in the Supra");
    }
    public static void playMusic() {
        System.out.println("Playing music inside the Supra");
    }
    public static void turnOnHeadlights() {
        System.out.println("Turning on the Supra's headlights");
    }

    public static void main(String[] args) {
        startEngine();
        accelerate();
        applyBrakes();
        drift();
        boostTurbo();
        checkFuelLevel();
        playMusic();
        turnOnHeadlights();
    }
}
