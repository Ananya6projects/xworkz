public class Ladder {
    public static void climb(int steps) {
        System.out.println("Climbing " + steps + " steps");
    }

    public static void fold(boolean isFolded) {
        System.out.println("Ladder folded: " + isFolded);
    }

    public static void height(double meters) {
        System.out.println("Ladder height: " + meters + " meters");
    }

    public static void material(String type) {
        System.out.println("Ladder material: " + type);
    }

    public static void weightCapacity(int kg) {
        System.out.println("Ladder weight capacity: " + kg + " kg");
    }

    public static void color(String ladderColor) {
        System.out.println("Ladder color: " + ladderColor);
    }

    public static void manufacturer(String name) {
        System.out.println("Ladder manufactured by: " + name);
    }
}
